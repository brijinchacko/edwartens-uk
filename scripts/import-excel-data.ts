  const calls25Data = XLSX.utils.sheet_to_json(wb.Sheets["2025 Completed calls"]);
  const calls25Result = await importDetailedLeads(calls25Data, "2025 Completed Calls");

  // Step 6: Import offline students
  const offResult = await importOfflineStudents(offlineData);

  // Summary
  console.log("\n====================================================");
  console.log("  IMPORT COMPLETE - SUMMARY");
  console.log("====================================================");
  console.log(`  Employees: ${empResult.created} created`);
  console.log(`  Batches: ${batchResult.created} created`);
  console.log(`  Students (Certified): ${studentResult.created} created`);
  console.log(`  Students (Offline): ${offResult.created} created`);
  console.log(`  Leads (RFQ): ${rfqResult.created} created`);
  console.log(`  Leads (Quality): ${qualResult.created} created`);
  console.log(`  Leads (2025 Calls): ${calls25Result.created} created`);

  // Final counts
  const totalUsers = await prisma.user.count();
  const totalStudents = await prisma.student.count();
