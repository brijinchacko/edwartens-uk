// ===================== HELPERS =====================

function normalizeEmail(e: string | null | undefined): string {
  return (e || "").toString().trim().toLowerCase();
}

function normalizePhone(p: string | number | null | undefined): string {
  return (p || "").toString().replace(/[\s\-\(\)]/g, "").trim();
}

function parseBatchDate(val: any): Date | null {
  if (!val) return null;

  // Excel serial number
  if (typeof val === "number" && val > 40000 && val < 50000) {
    return new Date((val - 25569) * 86400 * 1000);
  }

  // ISO date string
  if (typeof val === "string" && /^\d{4}-\d{2}-\d{2}/.test(val)) {
    return new Date(val);
  }

  // US format: 11/16/2024
  if (typeof val === "string" && /^\d{1,2}\/\d{1,2}\/\d{4}/.test(val)) {
    const [m, d, y] = val.split("/");
    return new Date(parseInt(y), parseInt(m) - 1, parseInt(d));
  }

  // UK format: "10th Mar 2025", "02nd Mar 2026", "9/16/2024"
  if (typeof val === "string") {
    const cleaned = val.replace(/(\d+)(st|nd|rd|th)\s/i, "$1 ");
    const d = new Date(cleaned);
    if (!isNaN(d.getTime())) return d;
  }

  return null;
}

function generateBatchName(date: Date): string {
  const year = date.getFullYear();
  const weekNum = Math.ceil(
    ((date.getTime() - new Date(year, 0, 1).getTime()) / 86400000 + 1) / 7
  );
  return `UK-PROF-W${weekNum}-${year}`;
}

