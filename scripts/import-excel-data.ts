// ===================== STEP 2: CREATE HISTORICAL BATCHES =====================

async function createHistoricalBatches(data: any[]) {
  console.log("\n========== STEP 2: CREATE HISTORICAL BATCHES ==========");

  const batchDates = new Map<string, Date>();

  for (const row of data) {
    const date = parseBatchDate(row.Batch);
    if (!date || isNaN(date.getTime())) continue;
    const name = generateBatchName(date);
    if (!batchDates.has(name)) {
      batchDates.set(name, date);
    }
  }

  let created = 0;
  let skipped = 0;

  for (const [name, startDate] of batchDates) {
    const existing = await prisma.batch.findFirst({ where: { name } });
    if (existing) {
      skipped++;
      continue;
    }

    const endDate = new Date(startDate);
    endDate.setDate(endDate.getDate() + 5); // 5-day course

    const isCompleted = startDate < new Date();

    await prisma.batch.create({
      data: {
        name,
        course: "PROFESSIONAL_MODULE",
        startDate,
        endDate,
        capacity: 6,
        status: isCompleted ? "COMPLETED" : "UPCOMING",
        location: "Milton Keynes",
        isAutoGenerated: false,
      },
    });
    created++;
  }

  console.log(`  Batches: ${created} created, ${skipped} already existed`);
  return { created, skipped };
}

// ===================== STEP 3: IMPORT CERTIFIED STUDENTS =====================

async function importStudents(data: any[]) {
  console.log("\n========== STEP 3: IMPORT CERTIFIED STUDENTS ==========");

  const defaultPassword = await bcrypt.hash("Student@2026!", 12);
  let created = 0;
  let skipped = 0;
  let errors = 0;

  // Get counsellor map
  const counsellors = await prisma.employee.findMany({
    include: { user: { select: { name: true, id: true } } },
  });
  const counsellorMap = new Map<string, string>();
  for (const c of counsellors) {
    const firstName = c.user.name.split(" ")[0].toLowerCase();
    counsellorMap.set(firstName, c.id);
  }

  for (const row of data) {
    try {
      const name = (row["Full Name"] || "").toString().trim();
      const email = normalizeEmail(row["Email ID"]);
      const phone = normalizePhone(row["Mobile Number"]);

      if (!name || !email) { skipped++; continue; }

      // Check if user already exists
      const existing = await prisma.user.findUnique({ where: { email } });
      if (existing) { skipped++; continue; }

      // Parse batch date
      const batchDate = parseBatchDate(row.Batch);
      let batchId: string | null = null;
      if (batchDate && !isNaN(batchDate.getTime())) {
        const batchName = generateBatchName(batchDate);
        const batch = await prisma.batch.findFirst({ where: { name: batchName } });
        batchId = batch?.id || null;
      }

      // Determine status
      const courseStatus = (row["Course status"] || "").toString().toLowerCase();
      let studentStatus: string;
      if (courseStatus === "completed") studentStatus = "COMPLETED";
      else if (courseStatus === "intraining" || courseStatus === "intraning") studentStatus = "ACTIVE";
      else if (row.Placed === "Yes") studentStatus = "ALUMNI";
      else studentStatus = "ACTIVE";

      // Map counsellor
      const counsellorName = (row.Counsellor || "").toString().toLowerCase().trim();
      const counsellorId = counsellorMap.get(counsellorName) || null;

      // Create user
      const user = await prisma.user.create({
        data: {
          name,
          email,
          phone: phone || null,
          password: defaultPassword,
          role: "STUDENT",
          isActive: true,
          onboarded: true,
        },
      });

      // Create student
      await prisma.student.create({
        data: {
          userId: user.id,
          batchId,
          course: "PROFESSIONAL_MODULE",
          status: studentStatus as any,
          currentPhase: courseStatus === "completed" ? 5 : 0,
          paymentStatus: "PAID",
          counsellorId,
          enrollmentDate: batchDate || new Date(),
        },
      });

      created++;
      if (created % 20 === 0) console.log(`  ... ${created} students created`);
    } catch (err: any) {
      errors++;
    }
  }

  console.log(`  Students: ${created} created, ${skipped} skipped, ${errors} errors`);
  return { created, skipped, errors };
}

// ===================== STEP 4: IMPORT RFQ LEADS =====================

async function importRFQLeads(data: any[]) {
  console.log("\n========== STEP 4: IMPORT RFQ LEADS ==========");

  // Build email index for dedup
  const existingEmails = new Set(
    (await prisma.lead.findMany({ select: { email: true } })).map((l) => l.email.toLowerCase())
  );
  const existingStudentEmails = new Set(
    (await prisma.user.findMany({ where: { role: "STUDENT" }, select: { email: true } })).map((u) => u.email.toLowerCase())
  );

  let created = 0;
  let skipped = 0;

  for (const row of data) {
    try {
      const name = (row["Full Name"] || "").toString().trim();
      const email = normalizeEmail(row["Email ID"]);
      const phone = normalizePhone(row["Mobile Number"]);
      const comments = (row.Comments || "").toString().trim();
      const source = (row.Source || "website").toString().trim();
      const counsellor = (row.counsellor || "").toString().trim();

      if (!name || !email) { skipped++; continue; }
      if (existingEmails.has(email) || existingStudentEmails.has(email)) { skipped++; continue; }

      await prisma.lead.create({
        data: {
          name,
          email,
          phone: phone || "",
          courseInterest: "PROFESSIONAL_MODULE",
          source: source || "Excel Import",
          status: comments?.toLowerCase().includes("enrolled") ? "ENROLLED" : "NEW",
        },
      });

      // Add notes if comments exist
      if (comments) {
        const lead = await prisma.lead.findFirst({ where: { email } });
        if (lead) {
          await prisma.leadNote.create({
            data: {
              leadId: lead.id,
              content: `[Excel Import] Counsellor: ${counsellor || "N/A"} | ${comments}`,
              createdBy: "Excel Import",
            },
          });
        }
      }

      existingEmails.add(email);
      created++;
      if (created % 100 === 0) console.log(`  ... ${created} leads imported`);
    } catch {
      skipped++;
    }
  }

  console.log(`  RFQ Leads: ${created} created, ${skipped} skipped`);
  return { created, skipped };
}

// ===================== STEP 5: IMPORT QUALITY + 2025 LEADS =====================

async function importDetailedLeads(data: any[], sheetName: string) {
  console.log(`\n========== STEP 5: IMPORT ${sheetName.toUpperCase()} ==========`);

  const existingEmails = new Set(
    (await prisma.lead.findMany({ select: { email: true } })).map((l) => l.email.toLowerCase())
  );

  let created = 0;
  let skipped = 0;
  let notesAdded = 0;

  for (const row of data) {
    try {
      const name = (row["Full Name"] || "").toString().trim();
      const email = normalizeEmail(row["Email ID"]);
      const phone = normalizePhone(row["Mobile No."] || row["Mobile Number"]);
      const counsellor = (row.Counselor || row.Counsellor || "").toString().trim();
      const visa = (row["Visa Status"] || "").toString().trim();
      const education = (row.Education || "").toString().trim();
      const experience = (row.Experience || "").toString().trim();
      const comment = (row.Comment || "").toString().trim();
      const interested = (row["Interested "] || row.Interested || "").toString().trim();
      const enrolled = (row.Enrolled || "").toString().trim();
      const usp = (row.USP || "").toString().trim();

      if (!name || !email) { skipped++; continue; }

      if (existingEmails.has(email)) {
        // Add notes to existing lead
        const existingLead = await prisma.lead.findFirst({ where: { email } });
        if (existingLead && comment) {
          await prisma.leadNote.create({
            data: {
              leadId: existingLead.id,
              content: `[${sheetName}] Counsellor: ${counsellor} | Visa: ${visa} | Education: ${education} | Experience: ${experience} | ${comment}`,
              createdBy: "Excel Import",
            },
          });
          notesAdded++;
        }
        skipped++;
        continue;
      }

      // Determine status
      let status = "NEW";
      if (enrolled?.toLowerCase() === "yes") status = "ENROLLED";
      else if (interested?.toLowerCase() === "yes") status = "QUALIFIED";
      else if (comment) status = "CONTACTED";

      await prisma.lead.create({
        data: {
          name,
          email,
          phone: phone || "",
          courseInterest: "PROFESSIONAL_MODULE",
          qualification: education || null,
          source: `Excel - ${sheetName}`,
          status: status as any,
        },
      });

      // Add detailed note
      const lead = await prisma.lead.findFirst({ where: { email } });
      if (lead) {
        const noteContent = [
          counsellor ? `Counsellor: ${counsellor}` : "",
          visa ? `Visa: ${visa}` : "",
          education ? `Education: ${education}` : "",
          experience ? `Experience: ${experience}` : "",
          usp ? `USP: ${usp}` : "",
          comment ? `Notes: ${comment}` : "",
        ].filter(Boolean).join(" | ");

        if (noteContent) {
          await prisma.leadNote.create({
            data: {
              leadId: lead.id,
              content: `[${sheetName}] ${noteContent}`,
              createdBy: "Excel Import",
            },
          });
          notesAdded++;
        }
      }

      existingEmails.add(email);
      created++;
    } catch {
      skipped++;
    }
  }

  console.log(`  ${sheetName}: ${created} created, ${skipped} skipped, ${notesAdded} notes added`);
  return { created, skipped, notesAdded };
}

// ===================== STEP 6: IMPORT OFFLINE STUDENTS =====================

